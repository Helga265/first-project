# Программа, що видаляє те що написано в перших дужках зліва
# введення даних
x = str(input('Введіть строку: '))
# виводемо текст від нульвого символу до симвлу на один назад від "("
start = x[:x.find('(')-1]
# виводемо текст від симвлу де стоїть дужка ")" (не враховужчи її) до останнього символу
finish = x[x.find(')')+1:]
# об'єднуємо оримані зміни і виводимо
print(f'Ваша строка без дужок виглядає так: {start}{finish}')

